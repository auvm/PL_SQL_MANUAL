--Definición de un procedimiento almacenado
--PROCEDIMIENTO GASTO TOTAL POR CLIENTE
CREATE OR REPLACE PROCEDURE CALCULO_GASTO (P_ID_CLIENTE IN CLIENTES.ID_CLIENTE%TYPE) --argumentos del procedimiento y su tipo de dato
AS
    --definición de variables locales
    V_GASTO_TOTAL NUMBER;
    V_NOMBRE_CLIENTE CLIENTES.NOMBRE%TYPE;
BEGIN
    --Consulta para calcular el gasto total del cliente ingresado
    SELECT cl.nombre, SUM(p.precio * pe.cantidad)
    INTO V_NOMBRE_CLIENTE, V_GASTO_TOTAL
    FROM pedidos pe
    JOIN productos p
    ON pe.id_producto = p.id_producto
    JOIN clientes cl
    on pe.id_cliente = cl.id_cliente
    WHERE pe.id_cliente = P_ID_CLIENTE
    GROUP BY cl.nombre;
 
    
    DBMS_OUTPUT.PUT_LINE('El cliente ' || V_NOMBRE_CLIENTE || ' gastó: $' || V_GASTO_TOTAL);
 
    --Excepción en caso de no existir el P_ID_CLIENTE ingresado
    EXCEPTION
        WHEN
            NO_DATA_FOUND
        THEN
            DBMS_OUTPUT.PUT_LINE('Error: No se encontró un cliente con el ID ' || P_ID_CLIENTE);
END;
 
--Llamada del procedimiento
BEGIN
    CALCULO_GASTO(1);
END;

--Definición de una función
--FUNCIÓN GASTO TOTAL POR CLIENTE
CREATE OR REPLACE FUNCTION F_CALCULO_GASTO(P_ID_CLIENTE IN CLIENTES.ID_CLIENTE%TYPE) --argumentos de la función y su tipo de dato
--tipo de dato que va a retornar la función 
RETURN NUMBER
--definición de variables locales
IS 
 V_GASTO_TOTAL NUMBER;
--consulta para calcular el gasto total del cliente ingresado
BEGIN
    SELECT SUM(p.precio * pe.cantidad)
    INTO V_GASTO_TOTAL
    FROM pedidos pe
    JOIN productos p
    ON pe.id_producto = p.id_producto
    WHERE pe.id_cliente = P_ID_CLIENTE;
    
    --retorna el valor del gasto total calculado
    RETURN V_GASTO_TOTAL;
 
END F_CALCULO_GASTO; --nombre de la función para hacer más legible el código
--indica que el END corresponde a la función F_CALCULO_GASTO


--Llamada de una función y mostrar su resultado
DECLARE
