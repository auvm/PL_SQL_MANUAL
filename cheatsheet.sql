GROUP BY NOMBRE;

--Consulta con JOIN entre tres tablas, agrupamiento, ordenamiento y solo el primer resultado
--MUESTRA CLIENTE CON MÁS PEDIDOS
SELECT CL.NOMBRE, 
       COUNT(PE.ID_PEDIDO) AS TOTAL_PEDIDOS
FROM CLIENTES CL, PEDIDOS PE
WHERE CL.ID_CLIENTE = PE.ID_CLIENTE
GROUP BY CL.ID_CLIENTE, CL.NOMBRE
ORDER BY TOTAL_PEDIDOS DESC;
FETCH FIRST 1 ROWS ONLY;

